wynik = 0

i = 0
while i < 4:
    x = int(input("Podaj liczbe: "))
    wynik += x
    i += 1
print("Wynik dodawania liczb to:",wynik)
# Wcięcia w Python są kluczowe. TAB przez edytor IDLE jest zamieniany
# automatycznie na 4 spacje. Kod umieszczony pod pętlą bez 4 spacji nie jest
# brany pod uwagę przez pętle.
